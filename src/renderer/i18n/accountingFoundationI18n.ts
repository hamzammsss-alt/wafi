function detectLocale(): Locale {
    const win = window as any;
    const fromI18n = String(win?.i18n?.language || '').toLowerCase();
    const fromDoc = String(document?.documentElement?.lang || '').toLowerCase();
    const fromStorage = String(localStorage.getItem('lang') || '').toLowerCase();
    const value = fromI18n || fromDoc || fromStorage || 'en';
    if (value.startsWith('ar')) return 'ar';
    if (value.startsWith('he')) return 'he';
    return 'en';
}

export function tAccountingFoundation(key: string): string {
    const win = window as any;
    if (win?.i18n && typeof win.i18n.t === 'function') {
        const translated = String(win.i18n.t(key) || '');
        if (translated && translated !== key) {
            return translated;
        }
    }
    const locale = detectLocale();
    return bundle[locale][key] || bundle.en[key] || key;
}
